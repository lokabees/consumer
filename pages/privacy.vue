        Single-Sign-On-Authentifizierung ist, dass die Nutzer bei dem jeweiligen
        Single-Sign-On-Anbieter registriert sind und die erforderlichen
        Zugangsdaten in dem dafür vorgesehenen Onlineformular eingeben, bzw.
        schon bei dem Single-Sign-On-Anbieter angemeldet sind und die
        Single-Sign-On-Anmeldung via Schaltfläche bestätigen.
      </p>
      <p><br /></p>
      <p>
        Die Authentifizierung erfolgt direkt bei dem jeweiligen
        Single-Sign-On-Anbieter. Im Rahmen einer solchen Authentifizierung
        erhalten wir eine Nutzer-ID mit der Information, dass der Nutzer unter
        dieser Nutzer-ID beim jeweiligen Single-Sign-On-Anbieter eingeloggt ist
        und eine für uns für andere Zwecke nicht weiter nutzbare ID (sog "User
        Handle“). Ob uns zusätzliche Daten übermittelt werden, hängt allein von
